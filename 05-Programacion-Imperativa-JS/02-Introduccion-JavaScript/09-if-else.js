// Estructura de control if-else
/* 
  La estructura de control if-else nos permite tomar decisiones en nuestro código, es decir,
  ejecutar un bloque de código si se cumple una condición y otro bloque de código si no se 
  cumple la condición.

  Sintaxis:
  if (condición) {
    bloque de código si se cumple la condición
  }
  else {
    bloque de código si no se cumple la condición
  }
*/

let edad = 4;

if (edad >= 18){
  console.log('Eres mayor de edad');
}else{
  console.log('Eres menor de edad');
}